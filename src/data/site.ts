for (const product of products) {
  const nameFallbacks: Partial<Record<Lang, string>> = {};
  const summaryFallbacks: Partial<Record<Lang, string>> = {};
  const detailFallbacks: Partial<Record<Lang, string>> = {};
  const materialFallbacks: Partial<Record<Lang, string>> = {};
  const edgeFallbacks: Partial<Record<Lang, string>> = {};
  const optionFallbacks: Partial<Record<Lang, readonly string[]>> = {};
  const optionKeys = fallbackProductOptionKeys[product.slug] ?? [];

  for (const lang of langCodes) {
    if (lang === defaultLang) continue;

    const productName = product.name as Record<string, string>;
    const productSummary = product.summary as Record<string, string>;
    const productDetail = product.detail as Record<string, string>;
    const productMaterial = product.materialNote as Record<string, string>;
    const productEdge = product.edgeNote as Record<string, string>;
    const productOptions = product.customOptions as unknown as Record<string, readonly string[]>;
    const fallbackCopy = fallbackProductText[lang]?.[product.slug];
    const displayName = productName[lang] || fallbackCopy?.name || product.name.en;
    const displaySummary = productSummary[lang] || fallbackCopy?.summary || product.summary.en;

    if (!productName[lang] && fallbackCopy?.name) nameFallbacks[lang] = fallbackCopy.name;
    if (!productSummary[lang] && fallbackCopy?.summary) summaryFallbacks[lang] = fallbackCopy.summary;
    if (!productDetail[lang] && fallbackProductDetailTemplates[lang]) detailFallbacks[lang] = fallbackProductDetailTemplates[lang](displayName, displaySummary);
    if (!productMaterial[lang] && fallbackProductMaterialTemplates[lang]) materialFallbacks[lang] = fallbackProductMaterialTemplates[lang](displayName);
    if (!productEdge[lang] && fallbackProductEdgeTemplates[lang]) edgeFallbacks[lang] = fallbackProductEdgeTemplates[lang](displayName);

    const optionLabels = fallbackProductOptionLabels[lang];
    const translatedOptions = optionLabels ? optionKeys.map((key) => optionLabels[key]).filter(Boolean) : [];
    if (!productOptions[lang] && translatedOptions.length) optionFallbacks[lang] = translatedOptions;
  }

  if (Object.keys(nameFallbacks).length) localizedStringFallbacks.set(product.name, nameFallbacks);
  if (Object.keys(summaryFallbacks).length) localizedStringFallbacks.set(product.summary, summaryFallbacks);
  if (Object.keys(detailFallbacks).length) localizedStringFallbacks.set(product.detail, detailFallbacks);
  if (Object.keys(materialFallbacks).length) localizedStringFallbacks.set(product.materialNote, materialFallbacks);
  if (Object.keys(edgeFallbacks).length) localizedStringFallbacks.set(product.edgeNote, edgeFallbacks);
  if (Object.keys(optionFallbacks).length) localizedListFallbacks.set(product.customOptions, optionFallbacks);
}

